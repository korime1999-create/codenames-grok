# Обратный словарь
WORD_TO_CATEGORIES = {}
for cat, words in CATEGORIES.items():
    for word in words:
        word_lower = word.lower().strip()
        if word_lower not in WORD_TO_CATEGORIES:
            WORD_TO_CATEGORIES[word_lower] = []
        WORD_TO_CATEGORIES[word_lower].append(cat)

def get_categories(word: str):
    return WORD_TO_CATEGORIES.get(word.lower().strip(), ["unknown"])

def get_primary_category(word: str):
    cats = get_categories(word)
    return cats[0] if cats else "unknown"

ALL_WORDS = sorted(set(sum(CATEGORIES.values(), [])))

